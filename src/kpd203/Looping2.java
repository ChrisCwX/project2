import java.util.Scanner;
public class Looping2 {
    public static void main(String[] args)
   {
       Scanner input=new Scanner(System.in);
       System.out.println("Nombor 1:");
       int no1=input.nextInt();
       System.out.println("Nombor 2:");
       int no2=input.nextInt();
       System.out.println("Nombor 3:");
       int no3=input.nextInt();
       System.out.println("Nombor 4:");
       int no4=input.nextInt();
       System.out.println("Nombor 5:");
       int no5=input.nextInt();
       
      int jwp=no1+no2+no3+no4+no5;
      System.out.println("Hasil Tambah Adalah ;"+jwp);
   }
}